    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if(evt.getClickCount() ==2){

            String repInvID = String.valueOf(jTable1.getValueAt(row, 0));

            if(repHis == null || ! repHis.isDisplayable()){
                repHis = new RepairInHisServices(this,String.valueOf(jTable1.getValueAt(row, 0)));
                repHis.setVisible(true);
                this.setEnabled(false);

                repHis.addWindowListener(new java.awt.event.WindowAdapter() {

                    @Override
                    public void windowClosing(java.awt.event.WindowEvent windowEvent){

                        setEnabled(true);
                        toFront();

                    }
                });

            }else{
                repHis.toFront();
                repHis.requestFocus();
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

