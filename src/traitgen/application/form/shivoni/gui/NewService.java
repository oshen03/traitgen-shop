    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String price = jFormattedTextField1.getText();

        if(name.isEmpty()){

            JOptionPane.showMessageDialog(this, "Please Enter Service Name","Warning", JOptionPane.WARNING_MESSAGE);

        }else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type a price ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (!price.matches(("^(0|[1-9]\\d*)?(\\.\\d+)?(?<=\\d)$"))) {
            JOptionPane.showMessageDialog(this, "Please type a valid price ", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{

            try {

                ResultSet rs = MySQL.executeSearch("SELECT * FROM `service` WHERE `name`='"+name+"'");

                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Service already exist","Warning", JOptionPane.WARNING_MESSAGE);
                }else{
                    MySQL.executeIUD("INSERT INTO `service` (`name`,`price`) VALUES ('"+name+"','"+price+"') ");
                    loadServices();
                    clearAll();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable2.getSelectedRow();
        String name = jTextField1.getText();
        String price = jFormattedTextField1.getText();
        if(row ==-1){
            JOptionPane.showMessageDialog(this,"Please select a service to update", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            try {

                ResultSet rs = MySQL.executeSearch("SELECT * FROM `service` WHERE `name`='"+name+"' AND `price`='"+price+"'");

                if(rs.next()){
                    JOptionPane.showMessageDialog(this,"Please change service charge before update", "Warning", JOptionPane.WARNING_MESSAGE);
                }else{
                    MySQL.executeIUD("UPDATE `service` SET `price`='"+price+"' WHERE `name`='"+name+"' ");
                    loadServices();
                }

            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable2.getSelectedRow();
        String name = jTextField1.getText();

        if(row ==-1){
            JOptionPane.showMessageDialog(this,"Please select a service to remove", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{

            try {
                MySQL.executeIUD("DELETE FROM `service` WHERE `name`='"+name+"'");
                loadServices();
                clearAll();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

