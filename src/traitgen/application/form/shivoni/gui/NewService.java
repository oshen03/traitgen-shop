    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int row = jTable2.getSelectedRow();

        jTextField1.setText(String.valueOf(jTable2.getValueAt(row, 1)));
        jFormattedTextField1.setText(String.valueOf(jTable2.getValueAt(row, 2)));

        jTextField1.setEditable(false);
        jButton1.setEnabled(false);

        if(evt.getClickCount()==2){
            if(repairInvoice!=null){
                repairInvoice.getjTextField6().setText(String.valueOf(jTable2.getValueAt(row, 0)));
                repairInvoice.getjTextField5().setText(String.valueOf(jTable2.getValueAt(row, 1)));
                repairInvoice.getjFormattedTextField1().setText(String.valueOf(jTable2.getValueAt(row, 2)));
                this.dispose();

            }
        }

    }//GEN-LAST:event_jTable2MouseClicked

