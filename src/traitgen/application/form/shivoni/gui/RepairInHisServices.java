    private void loapRepServices() {

        try {

            ResultSet rs = MySQL.executeSearch("SELECT * FROM `repair_complete_invoice_has_service` INNER JOIN `service` ON `service`.`id`=`repair_complete_invoice_has_service`.`service_id` WHERE `repair_complete_invoice_id`='"+jTextField1.getText()+"'  ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(rs.getString("service.name"));
                vector.add(rs.getString("service.price"));
               dtm.addRow(vector);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

